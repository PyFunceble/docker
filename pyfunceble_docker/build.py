import logging

from .base import Base
from .config.client import docker_api_client


class Build(Base):
    """
    The builder. Build a PyFunceble image.
    """

    def it(self) -> None:  # pylint: disable=invalid-name
        """
        Build the image.
        """

        logging.info("Started to build.")
        builder = docker_api_client.build(**self.build_method_args)

        for response in builder:
            self.log_response(response)

        if self.is_latest:
            docker_api_client.tag(
                self.build_method_args["tag"],
                self.build_method_args["tag"].split(":")[0],
                tag="latest",
                force=True,
            )

        logging.info("Finished to build.")
